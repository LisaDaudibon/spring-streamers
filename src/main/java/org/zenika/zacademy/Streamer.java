package org.zenika.zacademy;

public class Streamer {
    public String name;
    public int age;
    public int streamMoney;
    public String[] plateformes;


    public Streamer(String name, int age, int streamMoney, String[] plateformes) {
        this.name = name;
        this.age = age;
        this.streamMoney = streamMoney;
        this.plateformes = plateformes;
    }
}
